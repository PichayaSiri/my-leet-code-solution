import { threeSum } from "@daily";

describe("15", () => {
  test.each([
    {
      nums: [-1, 0, 1, 2, -1, -4],
      expected: [
        [-1, -1, 2],
        [-1, 0, 1],
      ],
    },
    {
      nums: [0, 1, 1],
      expected: [],
    },
    {
      nums: [0, 0, 0],
      expected: [[0, 0, 0]],
    },
    {
      nums: [-1, 0, 1, 2, -1, -4],
      expected: [
        [-1, -1, 2],
        [-1, 0, 1],
      ],
    },
    {
      nums: [0, 0, 0, 0],
      expected: [[0, 0, 0]],
    },
    {
      nums: [1, -1, -1, 0],
      expected: [[-1, 0, 1]],
    },
    {
      nums: [-2, 0, 1, 1, 2],
      expected: [
        [-2, 0, 2],
        [-2, 1, 1],
      ],
    },
    {
      nums: [2, -3, 0, -2, -5, -5, -4, 1, 2, -2, 2, 0, 2, -4, 5, 5, -10],
      expected: [
        [-10, 5, 5],
        [-5, 0, 5],
        [-4, 2, 2],
        [-3, -2, 5],
        [-3, 1, 2],
        [-2, 0, 2],
      ],
    },
    {
      nums: [-1, 0, 1, 2, -1, -4, -2, -3, 3, 0, 4],
      expected: [
        [-4, 0, 4],
        [-4, 1, 3],
        [-3, -1, 4],
        [-3, 0, 3],
        [-3, 1, 2],
        [-2, -1, 3],
        [-2, 0, 2],
        [-1, -1, 2],
        [-1, 0, 1],
      ],
    },
  ])("threeSum", ({ nums, expected }) => {
    expect(threeSum(nums)).toEqual(expected);
  });
});
